//Function to insert a node in the middle of the linked list.
Node* insertInMiddle(Node* head, int x)
{
// Code here
Node*temp=head;
int count=0;
while(temp!=NULL){
    count++;
    temp=temp->next;
}
int mid;
if(count%2==0) mid=count/2;
else mid=(count/2)+1;
count=1;
temp=head;
while(temp!=NULL){
    if(count==mid){
        Node*middle=new Node(x);
        middle->next=temp->next;
        temp->next=middle;
        break;
    }
    else{
        count++;
        temp=temp->next;
    }
}
return head;
}



